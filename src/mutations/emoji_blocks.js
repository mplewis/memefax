export default function bloodType (text) {
  return text
    .replace(/cool/gi, '🆒')
    .replace(/free/gi, '🆓')
    .replace(/sos/gi, '🆘')
    .replace(/new/gi, '🆕')
    .replace(/ng/gi, '🆖')
    .replace(/cl/gi, '🆑')
    .replace(/id/gi, '🆔')
    .replace(/ab/gi, '🆎')
    .replace(/ok/gi, '🆗')
    .replace(/10/gi, '🔟')
    .replace(/a/gi, '🅰️')
    .replace(/b/gi, '🅱️')
    .replace(/o/gi, '🅾️')
    .replace(/m/gi, 'Ⓜ️')
    .replace(/#/gi, '#️⃣')
    .replace(/0/gi, '0️⃣')
    .replace(/1/gi, '1️⃣')
    .replace(/2/gi, '2️⃣')
    .replace(/3/gi, '3️⃣')
    .replace(/4/gi, '4️⃣')
    .replace(/5/gi, '5️⃣')
    .replace(/6/gi, '6️⃣')
    .replace(/7/gi, '7️⃣')
    .replace(/8/gi, '8️⃣')
    .replace(/9/gi, '9️⃣')
    .replace(/m/gi, '🆙')
    .replace(/i/gi, 'ℹ️')
    .replace(/m/gi, '🆗')
    .replace(/p/gi, '🅿')
}
